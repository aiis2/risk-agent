// ─── 对比摘要（§6 compare）──────────────────────────────────────────────────

function compareReports(a: any, b: any) {
  const gapA = (a.allGaps ?? a.gaps ?? []);
  const gapB = (b.allGaps ?? b.gaps ?? []);
  const countBySev = (gaps: any[]) => ({
    critical: gaps.filter((g) => g.severity === 'critical').length,
    high:     gaps.filter((g) => g.severity === 'high').length,
    medium:   gaps.filter((g) => g.severity === 'medium').length,
    low:      gaps.filter((g) => g.severity === 'low').length,
  });
  const scenariosA = new Set((a.coverageMatrix ?? []).map((r: any) => r.scenarioName ?? r.scenarioId));
  const scenariosB = new Set((b.coverageMatrix ?? []).map((r: any) => r.scenarioName ?? r.scenarioId));
  const newScenarios = [...scenariosB].filter((s) => !scenariosA.has(s));
  const removedScenarios = [...scenariosA].filter((s) => !scenariosB.has(s));
  return {
    reportA: { reportId: a.reportId, businessName: a.businessName, score: a.overallScore, createdAt: a.createdAt, gapCount: gapA.length, bySeverity: countBySev(gapA) },
    reportB: { reportId: b.reportId, businessName: b.businessName, score: b.overallScore, createdAt: b.createdAt, gapCount: gapB.length, bySeverity: countBySev(gapB) },
    scoreDelta: (b.overallScore ?? 0) - (a.overallScore ?? 0),
    gapCountDelta: gapB.length - gapA.length,
    newScenarios,
    removedScenarios,
  };
}

export function registerReportRoutes(app: FastifyInstance, ctx: AppContext): void {
  const store = ctx.storage.getStructuredStore();

  // GET /api/reports — 报告列表
  app.get('/api/reports', async () => {
    const rows = await store.all<any>(
      `SELECT report_id, session_id, business_name, locale, overall_score, created_at FROM gap_reports ORDER BY created_at DESC LIMIT 100`
    );
    return rows.map((r: any) => ({
      reportId:     r.report_id,
      sessionId:    r.session_id,
      businessName: r.business_name,
      locale:       r.locale,
      overallScore: r.overall_score,
      createdAt:    r.created_at
    }));
  });

  // GET /api/reports/compare?r1=:id&r2=:id — 对比两次报告（06-output-reporting.md §6）
  app.get('/api/reports/compare', async (req, reply) => {
    const { r1, r2 } = (req.query ?? {}) as { r1?: string; r2?: string };
    if (!r1 || !r2) return reply.code(400).send({ error: 'r1 and r2 required' });
    const [rowA, rowB] = await Promise.all([
      store.get<any>(`SELECT * FROM gap_reports WHERE report_id=?`, [r1]),
      store.get<any>(`SELECT * FROM gap_reports WHERE report_id=?`, [r2]),
    ]);
    if (!rowA) return reply.code(404).send({ error: 'report_a_not_found' });
    if (!rowB) return reply.code(404).send({ error: 'report_b_not_found' });
    return compareReports(JSON.parse(rowA.payload_json), JSON.parse(rowB.payload_json));
  });

  // GET /api/reports/:id — 报告详情（含费用摘要，06-output-reporting.md §8）
  app.get('/api/reports/:id', async (req, reply) => {
    const { id } = req.params as { id: string };
    const { locale } = (req.query ?? {}) as { locale?: string };
    const row = await store.get<any>(`SELECT * FROM gap_reports WHERE report_id=?`, [id]);
    if (!row) return reply.code(404).send({ error: 'not_found' });
    const report = JSON.parse(row.payload_json);
    if (locale && locale !== report.locale) report.locale = locale;
    // 附加费用摘要（从 session_costs 表）
    const costRow = await store.get<any>(
      `SELECT * FROM session_costs WHERE session_id=? ORDER BY created_at DESC LIMIT 1`,
      [row.session_id]
    ).catch(() => null);
    if (costRow) {
      report.costSummary = {
        totalCostUsd: costRow.total_cost_usd ?? 0,
        totalInputTokens: costRow.total_input_tokens ?? 0,
        totalOutputTokens: costRow.total_output_tokens ?? 0,
        cacheReadTokens: costRow.cache_read_tokens ?? 0,
        totalApiDurationMs: costRow.total_api_duration_ms ?? 0,
        totalToolDurationMs: costRow.total_tool_duration_ms ?? 0,
        modelUsage: costRow.model_usage_json ? JSON.parse(costRow.model_usage_json) : [],
      };
    }
    return report;
  });

  // GET /api/reports/:id/markdown — 下载 Markdown（06-output-reporting.md §6）
  app.get('/api/reports/:id/markdown', async (req, reply) => {
    const { id } = req.params as { id: string };
    const { locale } = (req.query ?? {}) as { locale?: string };
    const row = await store.get<any>(`SELECT * FROM gap_reports WHERE report_id=?`, [id]);
    if (!row) return reply.code(404).send({ error: 'not_found' });
    const report = JSON.parse(row.payload_json);
    const markdown = renderReportMarkdown(report, locale ?? report.locale ?? 'zh-CN');
    reply.header('content-type', 'text/markdown; charset=utf-8');
    reply.header('content-disposition', `attachment; filename="${row.business_name}-report.md"`);
    reply.send(markdown);
  });

  // GET /api/reports/:id/export.md — 旧路径（保留向后兼容）
  app.get('/api/reports/:id/export.md', async (req, reply) => {
    const { id } = req.params as { id: string };
    const { locale } = (req.query ?? {}) as { locale?: string };
    const row = await store.get<any>(`SELECT * FROM gap_reports WHERE report_id=?`, [id]);
    if (!row) return reply.code(404).send({ error: 'not_found' });
    const report = JSON.parse(row.payload_json);
    const markdown = renderReportMarkdown(report, locale ?? report.locale ?? 'zh-CN');
    reply.header('content-type', 'text/markdown; charset=utf-8');
    reply.send(markdown);
  });

  // GET /api/reports/:id/html — 下载 HTML（06-output-reporting.md §2.2）
  app.get('/api/reports/:id/html', async (req, reply) => {
    const { id } = req.params as { id: string };
    const { locale } = (req.query ?? {}) as { locale?: string };
    const row = await store.get<any>(`SELECT * FROM gap_reports WHERE report_id=?`, [id]);
    if (!row) return reply.code(404).send({ error: 'not_found' });
    const report = JSON.parse(row.payload_json);
    const markdown = renderReportMarkdown(report, locale ?? report.locale ?? 'zh-CN');
    const bodyHtml = mdToHtml(markdown);
    const fullHtml = wrapHtmlReport(bodyHtml, `${report.businessName} — Risk Report`);
    reply.header('content-type', 'text/html; charset=utf-8');
    reply.header('content-disposition', `attachment; filename="${row.business_name}-report.html"`);
    reply.send(fullHtml);
  });

  // DELETE /api/reports/:id — 删除报告（06-output-reporting.md §6）
  app.delete('/api/reports/:id', async (req, reply) => {
    const { id } = req.params as { id: string };
    const row = await store.get<any>(`SELECT report_id FROM gap_reports WHERE report_id=?`, [id]);
    if (!row) return reply.code(404).send({ error: 'not_found' });
    await store.run(`DELETE FROM gap_reports WHERE report_id=?`, [id]);
    // 尝试从对象存储删除文件（可能不存在，忽略错误）
    try {
      const objectStore = ctx.storage.getObjectStore();
      await objectStore.delete(`reports/${id}/report.md`).catch(() => {});
      await objectStore.delete(`reports/${id}/report.html`).catch(() => {});
      await objectStore.delete(`reports/${id}/report.json`).catch(() => {});
    } catch { /* 对象存储可能不支持 delete，忽略 */ }
    return reply.code(204).send();
  });
}
